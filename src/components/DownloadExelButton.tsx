import React from 'react';
import api from '../api';
import '../styles/DownloadExcelButton.css';

const DownloadExcelButton: React.FC = () => {
    const handleDownload = async () => {
        try {
            // –û—Ç–ø—Ä–∞–≤–ª—è–µ–º GET-–∑–∞–ø—Ä–æ—Å –Ω–∞ backend –¥–ª—è —Å–∫–∞—á–∏–≤–∞–Ω–∏—è —Ñ–∞–π–ª–∞
            const response = await api.get('/items/download/excel', {
                responseType: 'blob', // –¢–∏–ø –æ—Ç–≤–µ—Ç–∞ blob –¥–ª—è –æ–±—Ä–∞–±–æ—Ç–∫–∏ —Ñ–∞–π–ª–æ–≤
            });

            // –ì–µ–Ω–µ—Ä–∏—Ä—É–µ–º —Å—Å—ã–ª–∫—É –¥–ª—è —Å–∫–∞—á–∏–≤–∞–Ω–∏—è —Ñ–∞–π–ª–∞
            const url = window.URL.createObjectURL(new Blob([response.data]));
            const link = document.createElement('a');
            link.href = url;
            link.setAttribute('download', 'items.xlsx'); // –ò–º—è —Ñ–∞–π–ª–∞
            document.body.appendChild(link);
            link.click();
            link.parentNode?.removeChild(link);
        } catch (error) {
            console.error('Error downloading Excel file:', error);
            alert('Failed to download file. Try again later.');
        }
    };

    return (
        <button className="download-excel-button" onClick={handleDownload}>
            üìä Download Excel
        </button>
    );
};

export default DownloadExcelButton;
